public class Increment_character
{
    public static void main(String args[])
    {
        char myChar1 = 'A';
        char myChar2 = 'B';

        System.out.println( "1st Character : " +myChar1 );
        System.out.println( "2nd Character : " +myChar2 );
        myChar2++; // Increment Character
        System.out.println( "Incremente to 3rd Character : " +myChar2 );
    }
}
