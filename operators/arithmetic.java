class Main {
    public static void main(String[] args) {

        // Declare variables
        int number1 = 10, number2 = 5;

        // Addition
        System.out.println("10 + 5 = " + (number1 + number2));

        // Subtraction
        System.out.println("10 - 5 = " + (number1 - number2));

        // Multiplication
        System.out.println("10 * 5 = " + (number1 * number2));

        // Division
        System.out.println("10 / 5 = " + (number1 / number2));

        // Module
        System.out.println("10 % 5 = " + (number1 % number2));

    }
}